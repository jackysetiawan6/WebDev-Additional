        <div class="w-100 p-5 d-flex flex-column" style="text-align: justify;">
            <p>Sebagai salah satu instansi keuangan terbesar di Indonesia, PT Bank Central Asia Tbk. (BCA) berkomitmen untuk ikut hadir memberikan dukungan bagi tumbuhnya budaya belajar di perusahaan, khususnya perbankan. Komitmen tersebut ditunjukkan lewat hadirnya BCA Learning Institute, sebuah tempat training center bagi karyawan perusahaan yang hadir lewat wujud bangunan ikonik baru yang berdiri di Sentul, Bogor. Semakin menjadi focal point, karena bangunan ini menerapkan pemilihan warna pada fasadnya yang mengikuti corporate colour, namun tetap senada dengan lingkungan sekitar yang bernuansa hijau dan dikelilingi pepohonan.</p>
            <p class=" fw-semibold">PEMBAGIAN DUA MASSA BANGUNAN</p>
            <p>BCA Learning Institute berlokasi di sebuah tanah kosong berkontur dan bertipe clay shale. Perusahaan menginginkan berdirinya sebuah tempat pelatihan yang integratif, sekaligus memiliki suasana yang nyaman untuk sebuah institusi pembelajaran. Selain itu, klien juga menginginkan sebuah tempat pembelajaran yang terbuka dan menjadi “gerbang, portal atau tangga” menuju sisi sosial dan ekonomi yang lebih baik.</p>
            <p>Sindhu Hadiprana Design Consultant bersama Parametr Architecture menangani sisi arsitektur proyek ini dengan mengusung konsep pembagian dua massa bangunan. Kedua massa tersebut dianalogikan sebagai sebuah portal atau gerbang masuk suatu ruang pembelajaran menuju puncak keberhasilan. Bentuk-bentuk titik keberhasilan dapat hadir lewat communal space atau area bersosialisasi pada ruang amphitheater.</p>
            <p>Bangunan sebelah utara memiliki 10 lantai, sementara di sisi barat mempunyai 7 lantai. Kedua bangunan tersebut terkoneksi lewat sebuah area pemersatu di lantai dasar dan sebuah gerbang di lantai kedua. Dari massanya sendiri, diterapkan sebuah konsep di mana bangunan ini memberikan space untuk keberadaan breathing room di setiap lantain yang berfungsi sebagai ruang-ruang komunal bangunan dan area lounge.</p>
            <p class="fw-semibold">ZONING VERTIKAL</p>
            <p> zoning-nya diterapkan secara vertikal, di mana semakin tinggi lantai, sisi privasinya semakin kuat. Di lantai dasar dibangun ruang publik, seperti lobby, ruang makan, dan BCA Gallery. Sebagai training center, bangunan ini dipenuhi oleh kelas-kelas yang dibagi menurut levelnya, mulai dari kelas standar, eksekutif dan ruang diskusi yang terdiri dari dua buah kelas: indoor dan outdoor (amphitheater). Di bagian atas gedung diletakkan ruang kantor manajemen yang diikuti dengan penempatan roof garden di setiap atap bangunan dan pada jembatan yang menghubungkan pada kedua massanya.</p>
            <p>Proses pengerjaannya memakan waktu 3 tahun, diawali dengan penyelidikan kondisi tanah pada tahun 2013, di mana hasil kondisi penelitian tersebut memengaruhi rancangan bangunan secara keseluruhan. Kondisi tanah yang bertipe clay shale membuat Sindhu Hadiprana Design Consultant dan Parametr Architecture tidak terlalu banyak mengubah kontur tanah yang ada.</p>
            <p>Hadirnya BCA Gallery yang berfungsi sebagai area untuk menyimpan informasi sejarah BCA secara interaktif dari awal didirikan hingga saat ini menjadikan sisi edukasi pada bangunan ini terasa sangat kental, tanpa harus melepaskan identitas perusahaan pada desain bangunan utamanya. BCA Learning Institute memang menjadi semacam mini museum yang dapat memberikan gambaran akan sejarah, perjalanan, dan pencapaian perusahaan selama hampir lebih dari 60 tahun dalam melayani kebutuhan perbankan di tanah air. Hal itu secara tepat terwujud lewat ekspresi dan filosofi yang kuat pada desain bangunannya.</p>
